package reCapDemo_Classes;

public class DortIslem {
	public int Topla(int sayi1, int sayi2) {
		return sayi1 + sayi2;
	}

	public int Çıkart(int sayi1, int sayi2) {
		return sayi1 - sayi2;
	}

	public int Çarp(int sayi1, int sayi2) {
		return sayi1 * sayi2;
	}

	public int Böl(int sayi1, int sayi2) {
		return sayi1 / sayi2;
	}

}
